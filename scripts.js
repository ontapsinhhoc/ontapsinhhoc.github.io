function generateAccordion(data, containerId) {
    const container = document.getElementById(containerId);
    container.innerHTML = '';

    data.forEach((chude, index) => {
        const accordion = document.createElement("div");
        accordion.className = "accordion";

        const header = document.createElement("div");
        header.className = "accordion-header";
        header.innerHTML = `${chude.chude} <span class="toggle-icon">+</span>`;
        header.onclick = () => toggleContent(index, header, containerId);

        const content = document.createElement("div");
        content.className = "accordion-content";
        content.style.display = "none";

        const ul = document.createElement("ul");
        chude.bai.forEach(bai => {
            const li = document.createElement("li");
            li.innerHTML = `${bai.ten}`;
            if (bai.pdf) {
                li.innerHTML += ` - <a href="${bai.pdf}" target="_blank">Tải tài liệu miễn phí</a>`;
            }
            if (bai.form) {
                li.innerHTML += ` - <a href="${bai.form}" target="_blank">Kiểm tra thử miễn phí</a>`;
            }
            if (bai.youtube) {
                li.innerHTML += ` - <a href="${bai.youtube}" target="_blank">Link Youtube</a>`;
            }
            ul.appendChild(li);
        });
        content.appendChild(ul);

        accordion.appendChild(header);
        accordion.appendChild(content);
        container.appendChild(accordion);
    });
}

function toggleContent(index, header, containerId) {
    const contents = document.querySelectorAll(`#${containerId} .accordion-content`);
    const icons = document.querySelectorAll(`#${containerId} .toggle-icon`);

    const content = contents[index];
    const icon = icons[index];

    if (content.style.display === "none") {
        content.style.display = "block";
        icon.innerText = "−";
    } else {
        content.style.display = "none";
        icon.innerText = "+";
    }

    header.classList.toggle("active");
}

function openTab(tabId) {
    document.querySelectorAll('.tab-content').forEach(content => {
        content.style.display = 'none';
    });
    document.getElementById(tabId).style.display = 'block';
	
    document.querySelectorAll('.tab-button').forEach(button => {
        button.classList.remove('active');
    });

    document.querySelector(`button[onclick="openTab('${tabId}')"]`).classList.add('active');

    if (tabId === 'hoc-sinh') {
        generateAccordion(hocSinhData, 'hoc-sinh-container');
    } else if (tabId === 'giao-vien') {
        generateAccordion(giaoVienData, 'giao-vien-container');
    } else if (tabId === 'van-ban') {
        generateAccordion(vanBanData, 'van-ban-container');
    } else if (tabId === 'tham-khao') {
        generateAccordion(boDeThamKhao, 'tham-khao-container');
    }
}

